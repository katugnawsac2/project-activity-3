# Define the class
class MyClass:
    # Define a method
    def method1(self):
        print("Method 1 is called.")

    # Define another method
    def method2(self):
        print("Method 2 is called.")

    # Define yet another method
    def method3(self):
        print("Method 3 is called.")

# Instantiate the class
my_object = MyClass()

# Call the methods using the object
my_object.method1()
my_object.method2()
my_object.method3()

